#Tipos de dados
#Para analisar dados, é importante saber com que tipo de dados estamos lidando.

#Podemos dividir os tipos de dados em três categorias principais:

# - numérico
# - categórico
# - ordinal
#Dados numéricos são números e podem ser divididos em duas categorias numéricas:

#Dados
#discretos - números limitados a inteiros. Exemplo: O número de carros passando.
#Dados
#Contínuos - números de valor infinito. Exemplo: O preço de um item, ou o tamanho de um item
#Dados categóricos são valores que não podem ser medidos uns contra os outros. Exemplo: um valor de cor, ou qualquer valor de sim/não.

#Dados ordinais são como dados categóricos, mas podem ser medidos uns contra os outros. Exemplo: notas escolares onde A é melhor que B e assim por diante.

#Ao conhecer o tipo de dados da sua fonte de dados, você poderá saber qual técnica usar ao analisá-los.

